package principal;

public abstract class Conta extends Pagavel{
    protected int dia;
    protected int mes;
    protected int ano;
    protected double valor;

   
    public Conta(int dia, int mes, int ano, double valor) {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
        this.valor = valor;
    }
    
    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }
    
    
}
