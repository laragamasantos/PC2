    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jogo.getTentativas() == 0 || jogo.isVenceu()) {
            jTextField1.setEditable(false);
            jTextField2.setEditable(false);
            jTextField3.setEditable(false);
            jTextField4.setEditable(false);
            jTextField5.setEditable(false);
            jTextField6.setEditable(false);
            jTextField7.setEditable(false);
        }
        else{
        ArrayList<String> tentativa = new ArrayList();

        tentativa.add(jTextField1.getText());
        tentativa.add(jTextField2.getText());
        tentativa.add(jTextField3.getText());
        tentativa.add(jTextField4.getText());
        tentativa.add(jTextField5.getText());
        tentativa.add(jTextField6.getText());
        tentativa.add(jTextField7.getText());

        ArrayList<Integer> colors = jogo.tentar(tentativa);

        jTextField1.setBackground(defineColor(colors.get(0)));
        jTextField2.setBackground(defineColor(colors.get(1)));
        jTextField3.setBackground(defineColor(colors.get(2)));
        jTextField4.setBackground(defineColor(colors.get(3)));
        jTextField5.setBackground(defineColor(colors.get(4)));
        jTextField6.setBackground(defineColor(colors.get(5)));
        jTextField7.setBackground(defineColor(colors.get(6)));

        jTextField8.setText(jogo.tentativas());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

    private Color defineColor(int i) {
        switch (i) {
            case 0:
                return Color.WHITE;
            case 1:
                return Color.BLUE;
            case 2:
                return Color.YELLOW;
            default:
                return Color.RED;

        }
    }

